def format_status(manifest: Manifest, show_details: bool = False) -> str:
    """Format manifest for CLI status command output.

    Args:
        manifest: Manifest object
        show_details: Whether to include detailed stage information

    Returns:
        Formatted status string for CLI display with comprehensive pipeline state
    """
    lines = []

    # === Section 1: Session Information ===
    lines.append("=" * 60)
    lines.append("PIPELINE STATUS REPORT")
    lines.append("=" * 60)
    lines.append("")
    lines.append("SESSION INFORMATION")
    lines.append("-" * 60)
    lines.append(f"Session ID:    {manifest.session_id}")

    # Status with visual indicator
    status_icon = {
        "not_started": "○",
        "running": "◐",
        "successful": "✓",
        "failed": "✗",
        "partial_success": "⚠",
        "completed": "✓",
        "completed_with_issues": "⚠",
    }.get(manifest.status, "?")
    lines.append(f"Overall Status: [{status_icon}] {manifest.status}")

    created_at = (
        manifest.created_at.isoformat(timespec="seconds")
        if manifest.created_at
        else "N/A"
    )
    updated_at = (
        manifest.updated_at.isoformat(timespec="seconds")
        if manifest.updated_at
        else "N/A"
    )
    lines.append(f"Created:       {created_at}")
    lines.append(f"Last Updated:  {updated_at}")

    # === Section 2: Input Information ===
    lines.append("")
    lines.append("INPUT INFORMATION")
    lines.append("-" * 60)
    lines.append(f"File Path: {manifest.input_path}")
    lines.append(f"File Hash: {manifest.input_hash[:16]}...{manifest.input_hash[-16:]}")

    # === Section 3: Stage Progress Table ===
    lines.append("")
    lines.append("STAGE PROGRESS")
    lines.append("-" * 60)

    # Table header
    lines.append(f"{'Stage':<12} {'Status':<15} {'Timestamp':<20} {'Output File'}")
    lines.append("-" * 60)

    stage_order = ["ingest", "summarize", "stage", "final", "audit"]
    for stage_name in stage_order:
        stage_info = manifest.stages.get(stage_name)

        if stage_info:
            status = stage_info.status
            # Visual status indicator
            status_display = {
                "not_started": "○ Not Started",
                "running": "◐ Running",
                "successful": "✓ Success",
                "failed": "✗ Failed",
                "skipped": "- Skipped",
            }.get(status, f"? {status}")

            timestamp = "—"
            if stage_info.timestamp:
                timestamp = stage_info.timestamp.strftime("%Y-%m-%d %H:%M:%S")

            output_file = "—"
            if stage_info.output_file:
                # Truncate long paths for display
                output_file = stage_info.output_file
                if len(output_file) > 40:
                    output_file = "..." + output_file[-37:]

            lines.append(
                f"{stage_name:<12} {status_display:<15} {timestamp:<20} {output_file}"
            )

    # === Section 4: Statistics Summary ===
    lines.append("")
    lines.append("STATISTICS")
    lines.append("-" * 60)

    total_parts = manifest.total_parts if manifest.total_parts is not None else "N/A"
    total_stages = manifest.total_stages if manifest.total_stages is not None else "N/A"
    estimated_tokens = (
        manifest.estimated_tokens if manifest.estimated_tokens is not None else "N/A"
    )

    lines.append(f"Total Parts:        {total_parts}")
    lines.append(f"Total Stages:       {total_stages}")
    lines.append(f"Estimated Tokens:   {estimated_tokens}")

    # Calculate time elapsed since creation
    if manifest.created_at:
        now = datetime.now()
        created = manifest.created_at
        if isinstance(created, str):
            created = datetime.fromisoformat(created)
        elapsed = now - created
        days = elapsed.days
        hours, remainder = divmod(int(elapsed.total_seconds()), 3600)
        minutes, seconds = divmod(remainder, 60)

        if days > 0:
            elapsed_str = f"{days}d {hours}h {minutes}m {seconds}s"
        elif hours > 0:
            elapsed_str = f"{hours}h {minutes}m {seconds}s"
        elif minutes > 0:
            elapsed_str = f"{minutes}m {seconds}s"
        else:
            elapsed_str = f"{seconds}s"
        lines.append(f"Time Elapsed:       {elapsed_str}")

    # === Section 5: Error Summary ===
    lines.append("")
    lines.append("ERROR SUMMARY")
    lines.append("-" * 60)

    failed_stages = [
        (name, info)
        for name, info in manifest.stages.items()
        if info and info.status == "failed"
    ]
    stage_with_errors = [
        (name, info) for name, info in manifest.stages.items() if info and info.error
    ]

    if failed_stages or stage_with_errors:
        for stage_name, info in failed_stages:
            lines.append(f"✗ FAILED: {stage_name.upper()}")
            lines.append(f"  Status: {info.status}")
            if info.error:
                lines.append(f"  Error: {info.error}")
            if info.timestamp:
                lines.append(f"  Time: {info.timestamp.strftime('%Y-%m-%d %H:%M:%S')}")
            lines.append("")

        # Show stages with errors but not necessarily failed
        for stage_name, info in stage_with_errors:
            if info.status != "failed" and info.error:
                lines.append(f"⚠ WARNING: {stage_name.upper()}")
                lines.append(f"  Status: {info.status}")
                lines.append(f"  Error: {info.error}")
                lines.append("")
    else:
        lines.append("✓ No errors detected")

    # === Section 6: Next Action Suggestion ===
    lines.append("")
    lines.append("NEXT ACTION")
    lines.append("-" * 60)

    # Determine next action based on current state
    next_action = _determine_next_action(manifest)
    lines.append(next_action)

    lines.append("")
    lines.append("=" * 60)

    return "\n".join(lines)


def _determine_next_action(manifest: Manifest) -> str:
    """Determine the next suggested action based on pipeline state.

    Args:
        manifest: Manifest object to analyze

    Returns:
        Suggested next action string
    """
    # Import here to avoid potential circular dependency
    from .models import StageInfo

    # Check if already completed
    if manifest.status in ["completed", "successful"]:
        return (
            "✓ Pipeline completed successfully. Review output in final analysis file."
        )

    # Check for failures
    failed_stages = [
        name
        for name, info in manifest.stages.items()
        if info and info.status == "failed"
    ]

    if failed_stages:
        failed_stage = failed_stages[0]
        if failed_stage == "ingest":
            return "✗ Ingest stage failed. Check input file and configuration. Retry with: longtext run <input-file>"
        elif failed_stage == "summarize":
            return "✗ Summarize stage failed. Consider retrying or check LLM configuration. Resume with: --resume"
        elif failed_stage == "stage":
            return "✗ Stage synthesis failed. Check summary files and retry. Resume with: --resume"
        elif failed_stage == "final":
            return "✗ Final analysis failed. Stage syntheses may be incomplete. Resume with: --resume"
        else:
            return f"✗ {failed_stage} stage failed. Review error details above. Resume with: --resume"

    # Check overall status for partial success
    if manifest.status == "partial_success":
        return "⚠ Pipeline completed with partial success. Review completed stages and errors. Continue with: --resume"

    if manifest.status == "completed_with_issues":
        return "⚠ Pipeline completed with warnings. Review the final output and warning details above."

    # Check which stage we're at
    if (
        manifest.stages.get(
            "ingest", StageInfo(name="ingest", status="not_started")
        ).status
        == "not_started"
    ):
        return "▶ Start pipeline with: longtext run <input-file> --config <config.yaml>"

    if (
        manifest.stages.get(
            "summarize", StageInfo(name="summarize", status="not_started")
        ).status
        == "running"
    ):
        return "◐ Summarization in progress. Wait for completion or check logs for details."

    if (
        manifest.stages.get(
            "summarize", StageInfo(name="summarize", status="not_started")
        ).status
        == "not_started"
    ):
        return "▶ Continue with: longtext run <input-file> --resume"

    if (
        manifest.stages.get(
            "stage", StageInfo(name="stage", status="not_started")
        ).status
        == "running"
    ):
        return "◐ Stage synthesis in progress. Wait for completion or check logs for details."

    if (
        manifest.stages.get(
            "stage", StageInfo(name="stage", status="not_started")
        ).status
        == "not_started"
    ):
        return "▶ Continue with: --resume to proceed with stage synthesis"

    if (
        manifest.stages.get(
            "final", StageInfo(name="final", status="not_started")
        ).status
        == "running"
    ):
        return "◐ Final analysis in progress. Wait for completion or check logs for details."

    if (
        manifest.stages.get(
            "final", StageInfo(name="final", status="not_started")
        ).status
        == "not_started"
    ):
        return "▶ Continue with: --resume to proceed with final analysis"

    if (
        manifest.stages.get("audit", StageInfo(name="audit", status="skipped")).status
        == "skipped"
    ):
        return "✓ Pipeline complete. Optional: Run audit stage with: --audit"

    return "? Pipeline state unclear. Review manifest or start fresh with: longtext run <input-file>"


def format_output_type(output_type: str, content: str, **kwargs) -> str:
    """Generic output formatter dispatcher.

    Args:
        output_type: Type of output ('summary', 'stage', 'final', 'manifest')
        content: Content to format (can be object or plain text)
        **kwargs: Additional arguments for formatting

    Returns:
        Formatted output string
    """
    if output_type == "summary":
        if isinstance(content, Summary):
            return render_summary(content, **kwargs)
        return content

    elif output_type == "stage":
        if isinstance(content, StageSummary):
            return render_stage(content, **kwargs)
        return content

    elif output_type == "final":
        if isinstance(content, FinalAnalysis):
            return render_final(content, **kwargs)
        return content

    elif output_type == "manifest":
        if isinstance(content, Manifest):
            return render_manifest_status(content)
        return content

    elif output_type == "status":
        if isinstance(content, Manifest):
            return format_status(content, kwargs.get("show_details", False))
        return content

    else:
        return f"Unknown output type: {output_type}\n\n{content}"
