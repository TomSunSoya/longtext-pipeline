# === Helper functions ===


def _get_timestamp() -> str:
    """Get current timestamp in ISO 8601 format."""
    return datetime.now().isoformat(timespec="seconds")


def _format_list(items: List[str], prefix: str = "- ") -> str:
    """Format a list of strings as a bullet list."""
    if not items:
        return "(none)"
    return "\n".join(f"{prefix}{item}" for item in items)


def _format_dict(data: Dict, indent: int = 0) -> str:
    """Format a dictionary as indented key-value pairs."""
    if not data:
        return "(none)"

    indent_str = "  " * indent
    lines = []
    for key, value in data.items():
        if isinstance(value, dict):
            lines.append(f"{indent_str}{key}:")
            lines.append(_format_dict(value, indent + 1))
        else:
            lines.append(f"{indent_str}{key}: {value}")
    return "\n".join(lines)


def _safe_get(data: Dict, *keys, default: str = "(not available)") -> str:
    """Safely get nested dictionary values with default fallback."""
    result: Any = data
    for key in keys:
        if isinstance(result, dict):
            result = result.get(key, default)
        else:
            return default
    return str(result) if result is not None else default


def _format_stage_info(info: Optional[StageInfo]) -> str:
    """Format a StageInfo object as a readable string."""
    if info is None:
        return "  - Status: (not tracked)"

    lines = [
        f"  - Status: {info.status}",
    ]

    if info.input_file:
        lines.append(f"  - Input: {info.input_file}")
    if info.output_file:
        lines.append(f"  - Output: {info.output_file}")
    if info.timestamp:
        lines.append(f"  - Timestamp: {info.timestamp.isoformat(timespec='seconds')}")
    if info.error:
        lines.append(f"  - Error: {info.error}")

    return "\n".join(lines)


