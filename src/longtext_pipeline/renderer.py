def render_summary(summary: Summary, model: str = "unknown") -> str:
    """Format a Summary object as markdown.

    Args:
        summary: Summary object to render
        model: LLM model name used for generation

    Returns:
        Formatted markdown string following DATA_MODEL.md Summary format
    """
    # Extract parts from content or use defaults
    content = summary.content.strip()

    # Try to parse structured sections (Key Points, Entities, etc.)
    sections = {
        "key_points": "(none)",
        "entities": "(none)",
        "themes": "(none)",
        "action_items": "(none)",
        "notes": "(none)",
    }

    # Simple section extraction from markdown content
    current_section = "notes"
    current_lines: List[str] = []

    for line in content.split("\n"):
        line_stripped = line.strip()

        if line_stripped.startswith("## "):
            section_name = line_stripped[3:].lower()
            # Save previous section
            if current_lines and current_section in sections:
                sections[current_section] = "\n".join(current_lines)
            # Start new section
            current_section = section_name.replace(" ", "_")
            current_lines = []
        elif line_stripped and not line_stripped.startswith("#"):
            current_lines.append(line)

    # Save last section
    if current_lines and current_section in sections:
        sections[current_section] = "\n".join(current_lines)

    # Build formatted output
    return SUMMARY_TEMPLATE.format(
        part_index=summary.part_index,
        timestamp=_get_timestamp(),
        part_file=f"part_{summary.part_index:02d}.txt",
        token_count=summary.metadata.get("token_count", "(estimating)"),
        model=model,
        key_points=sections["key_points"],
        entities=sections["entities"],
        themes=sections["themes"],
        action_items_header="(if applicable)",
        action_items=sections["action_items"],
        notes=sections["notes"],
    )


def render_stage(stage: StageSummary, model: str = "unknown") -> str:
    """Format a StageSummary object as markdown.

    Args:
        stage: StageSummary object to render
        model: LLM model name used for generation

    Returns:
        Formatted markdown string following DATA_MODEL.md Stage format
    """
    # Extract synthesis content
    synthesis = stage.synthesis.strip()

    # Try to parse structured sections
    sections: Dict[str, str] = {
        "executive_summary": "(none)",
        "consolidated_points": "(none)",
        "entity_synthesis": "(none)",
        "theme_evolution": "(none)",
        "consistency_checks": "(none)",
        "action_items_tracking": "(none)",
    }

    current_section = "executive_summary"
    current_lines: List[str] = []

    for line in synthesis.split("\n"):
        line_stripped = line.strip()

        if line_stripped.startswith("## "):
            section_name = line_stripped[3:].lower()
            if current_lines and current_section in sections:
                sections[current_section] = "\n".join(current_lines)
            current_section = section_name.replace(" ", "_")
            current_lines = []
        elif line_stripped and not line_stripped.startswith("#"):
            current_lines.append(line)

    if current_lines and current_section in sections:
        sections[current_section] = "\n".join(current_lines)

    # Build summary references
    summary_files = []
    for s in stage.summaries:
        summary_files.append(
            f"[summary_{s.part_index:02d}.md](./summary_{s.part_index:02d}.md)"
        )

    # Calculate token count
    token_count = stage.metadata.get("token_count", "(estimating)")

    return STAGE_TEMPLATE.format(
        stage_index=stage.stage_index,
        timestamp=_get_timestamp(),
        combined_from=", ".join(summary_files) if summary_files else "(none)",
        summary_count=len(stage.summaries),
        token_count=token_count,
        model=model,
        executive_summary=sections["executive_summary"],
        consolidated_points=sections["consolidated_points"],
        entity_synthesis=sections["entity_synthesis"],
        theme_evolution=sections["theme_evolution"],
        consistency_checks=sections["consistency_checks"],
        action_items_tracking=sections["action_items_tracking"],
    )


def render_final(
    final: FinalAnalysis, input_path: str = "(unknown)", model: str = "unknown"
) -> str:
    """Format a FinalAnalysis object as markdown.

    Args:
        final: FinalAnalysis object to render
        input_path: Path to original input file
        model: LLM model name used for generation

    Returns:
        Formatted markdown string following DATA_MODEL.md Final Analysis format
    """
    # Extract final result content
    final_result = final.final_result.strip()

    # Try to parse structured sections
    sections: Dict[str, str] = {
        "executive_summary": "(none)",
        "theme_analysis": "(none)",
        "participant_analysis": "(none)",
        "timeline_reconstruction": "(none)",
        "anomalies": "(none)",
        "recommendations": "(none)",
        "confidence_assessment": "(none)",
    }

    current_section = "executive_summary"
    current_lines: List[str] = []

    for line in final_result.split("\n"):
        line_stripped = line.strip()

        if line_stripped.startswith("## "):
            section_name = line_stripped[3:].lower()
            if current_lines and current_section in sections:
                sections[current_section] = "\n".join(current_lines)
            current_section = section_name.replace(" ", "_")
            current_lines = []
        elif line_stripped and not line_stripped.startswith("#"):
            current_lines.append(line)

    if current_lines and current_section in sections:
        sections[current_section] = "\n".join(current_lines)

    # Calculate derived values
    # Handle both forward and backward slashes for cross-platform compatibility
    input_filename = (
        input_path.split("/")[-1].split("\\")[-1]
        if "/" in input_path or "\\" in input_path
        else input_path
    )

    parts_count = 0
    for stage in final.stages:
        parts_count += len(stage.summaries)

    stages_count = len(final.stages)

    # Calculate tokens from all stages
    total_tokens = 0
    for stage in final.stages:
        for summary in stage.summaries:
            total_tokens += summary.metadata.get("token_count", 0)
        total_tokens += stage.metadata.get("token_count", 0)
    total_tokens += final.metadata.get("token_count", 0)

    # Get models used from metadata
    models_used = (
        final.metadata.get("models_used", [model])
        if isinstance(final.metadata.get("models_used"), list)
        else model
    )
    if isinstance(models_used, list):
        models_used = ", ".join(str(m) for m in models_used)

    # Calculate processing time
    created_at = final.metadata.get("created_at")
    completed_at = final.metadata.get("completed_at")

    processing_time = "(unknown)"
    if created_at and completed_at:
        try:
            if isinstance(created_at, str):
                created_at = datetime.fromisoformat(created_at.replace("Z", "+00:00"))
            if isinstance(completed_at, str):
                completed_at = datetime.fromisoformat(
                    completed_at.replace("Z", "+00:00")
                )

            delta = completed_at - created_at
            processing_time = str(int(delta.total_seconds()))
        except (ValueError, TypeError):
            processing_time = "(calculation failed)"

    return FINAL_TEMPLATE.format(
        input_filename=input_filename,
        timestamp=_get_timestamp(),
        input_path=input_path,
        parts_count=parts_count,
        stages_count=stages_count,
        processing_time=processing_time,
        tokens_analyzed=total_tokens,
        models_used=models_used,
        executive_summary=sections["executive_summary"],
        theme_analysis=sections["theme_analysis"],
        participant_analysis=sections["participant_analysis"],
        timeline_reconstruction=sections["timeline_reconstruction"],
        anomalies=sections["anomalies"],
        recommendations=sections["recommendations"],
        confidence_assessment=sections["confidence_assessment"],
        model=model,
    )


def render_manifest_status(manifest: Manifest) -> str:
    """Format a Manifest object as a status report.

    Args:
        manifest: Manifest object to render

    Returns:
        Formatted status report string
    """
    # Build stage status section
    stage_lines = []
    stage_order = ["ingest", "summarize", "stage", "final", "audit"]

    for stage_name in stage_order:
        stage_info = manifest.stages.get(stage_name)
        status_str = _format_stage_info(stage_info)
        stage_lines.append(f"{stage_name}:")
        stage_lines.append(status_str)

    stage_status = "\n".join(stage_lines)

    # Build error summary
    error_lines = []
    for stage_name, stage_info in manifest.stages.items():
        if stage_info and stage_info.error:
            error_lines.append(f"ERROR in {stage_name} stage:")
            error_lines.append(f"  {stage_info.error}")

    error_summary = "\n".join(error_lines) if error_lines else "(no errors)"

    # Format timestamps
    created_at = (
        manifest.created_at.isoformat(timespec="seconds")
        if manifest.created_at
        else "(unknown)"
    )
    updated_at = (
        manifest.updated_at.isoformat(timespec="seconds")
        if manifest.updated_at
        else "(unknown)"
    )

    # Format token counts
    total_parts = manifest.total_parts if manifest.total_parts else "(not tracked)"
    total_stages = manifest.total_stages if manifest.total_stages else "(not tracked)"
    estimated_tokens = (
        manifest.estimated_tokens if manifest.estimated_tokens else "(not tracked)"
    )

    return MANIFEST_STATUS_TEMPLATE.format(
        session_id=manifest.session_id,
        overall_status=manifest.status,
        input_path=manifest.input_path,
        created_at=created_at,
        updated_at=updated_at,
        total_parts=total_parts,
        total_stages=total_stages,
        estimated_tokens=estimated_tokens,
        stage_status=stage_status,
        error_summary=error_summary,
    )


